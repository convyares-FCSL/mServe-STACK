#pragma once

#include <array>

namespace mserve_display {

// Hand-defined 5x7 bitmap font — not a vendored library (avoids reproducing
// an 8x8 font table from memory with no way to verify transcription
// accuracy). Covers exactly what this UI needs: digits, uppercase Latin
// (Framebuffer::drawText upper-cases everything — no lowercase glyphs, a
// deliberate simplification for a small robot status screen, not an
// oversight), and a handful of punctuation (space . : - / %). Any
// unsupported character renders as blank rather than a garbage glyph.
//
// Each glyph is 7 rows; each row's bits 4..0 are pixel columns, left to
// right (bit 4 = leftmost column).
constexpr int kFontCols = 5;
constexpr int kFontRows = 7;

using Glyph = std::array<uint8_t, kFontRows>;

inline const Glyph & glyphFor(char c)
{
  static const Glyph kBlank = {0, 0, 0, 0, 0, 0, 0};

  static const Glyph k0 = {0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E};
  static const Glyph k1 = {0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E};
  static const Glyph k2 = {0x0E, 0x11, 0x01, 0x02, 0x04, 0x08, 0x1F};
  static const Glyph k3 = {0x1E, 0x01, 0x06, 0x01, 0x01, 0x11, 0x0E};
  static const Glyph k4 = {0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02};
  static const Glyph k5 = {0x1F, 0x10, 0x1E, 0x01, 0x01, 0x11, 0x0E};
  static const Glyph k6 = {0x06, 0x08, 0x10, 0x1E, 0x11, 0x11, 0x0E};
  static const Glyph k7 = {0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08};
  static const Glyph k8 = {0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E};
  static const Glyph k9 = {0x0E, 0x11, 0x11, 0x0F, 0x01, 0x02, 0x0C};

  static const Glyph kA = {0x04, 0x0A, 0x11, 0x11, 0x1F, 0x11, 0x11};
  static const Glyph kB = {0x1E, 0x11, 0x11, 0x1E, 0x11, 0x11, 0x1E};
  static const Glyph kC = {0x0E, 0x11, 0x10, 0x10, 0x10, 0x11, 0x0E};
  static const Glyph kD = {0x1E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x1E};
  static const Glyph kE = {0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x1F};
  static const Glyph kF = {0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x10};
  static const Glyph kG = {0x0E, 0x11, 0x10, 0x17, 0x11, 0x11, 0x0F};
  static const Glyph kH = {0x11, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11};
  static const Glyph kI = {0x0E, 0x04, 0x04, 0x04, 0x04, 0x04, 0x0E};
  static const Glyph kJ = {0x07, 0x02, 0x02, 0x02, 0x02, 0x12, 0x0C};
  static const Glyph kK = {0x11, 0x12, 0x14, 0x18, 0x14, 0x12, 0x11};
  static const Glyph kL = {0x10, 0x10, 0x10, 0x10, 0x10, 0x10, 0x1F};
  static const Glyph kM = {0x11, 0x1B, 0x15, 0x11, 0x11, 0x11, 0x11};
  static const Glyph kN = {0x11, 0x19, 0x15, 0x13, 0x11, 0x11, 0x11};
  static const Glyph kO = {0x0E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E};
  static const Glyph kP = {0x1E, 0x11, 0x11, 0x1E, 0x10, 0x10, 0x10};
  static const Glyph kQ = {0x0E, 0x11, 0x11, 0x11, 0x15, 0x12, 0x0D};
  static const Glyph kR = {0x1E, 0x11, 0x11, 0x1E, 0x14, 0x12, 0x11};
  static const Glyph kS = {0x0F, 0x10, 0x10, 0x0E, 0x01, 0x01, 0x1E};
  static const Glyph kT = {0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x04};
  static const Glyph kU = {0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E};
  static const Glyph kV = {0x11, 0x11, 0x11, 0x11, 0x11, 0x0A, 0x04};
  static const Glyph kW = {0x11, 0x11, 0x11, 0x15, 0x15, 0x1B, 0x11};
  static const Glyph kX = {0x11, 0x0A, 0x04, 0x04, 0x04, 0x0A, 0x11};
  static const Glyph kY = {0x11, 0x0A, 0x04, 0x04, 0x04, 0x04, 0x04};
  static const Glyph kZ = {0x1F, 0x01, 0x02, 0x04, 0x08, 0x10, 0x1F};

  static const Glyph kDot   = {0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0x00};
  static const Glyph kColon = {0x00, 0x04, 0x00, 0x00, 0x04, 0x00, 0x00};
  static const Glyph kDash  = {0x00, 0x00, 0x00, 0x1F, 0x00, 0x00, 0x00};
  static const Glyph kSlash = {0x01, 0x02, 0x04, 0x08, 0x10, 0x00, 0x00};
  static const Glyph kPct   = {0x11, 0x01, 0x02, 0x04, 0x08, 0x10, 0x11};
  static const Glyph kBang  = {0x04, 0x04, 0x04, 0x04, 0x04, 0x00, 0x04};

  if (c >= 'a' && c <= 'z') {
    c = static_cast<char>(c - 'a' + 'A');
  }

  switch (c) {
    case '0': return k0; case '1': return k1; case '2': return k2;
    case '3': return k3; case '4': return k4; case '5': return k5;
    case '6': return k6; case '7': return k7; case '8': return k8;
    case '9': return k9;
    case 'A': return kA; case 'B': return kB; case 'C': return kC;
    case 'D': return kD; case 'E': return kE; case 'F': return kF;
    case 'G': return kG; case 'H': return kH; case 'I': return kI;
    case 'J': return kJ; case 'K': return kK; case 'L': return kL;
    case 'M': return kM; case 'N': return kN; case 'O': return kO;
    case 'P': return kP; case 'Q': return kQ; case 'R': return kR;
    case 'S': return kS; case 'T': return kT; case 'U': return kU;
    case 'V': return kV; case 'W': return kW; case 'X': return kX;
    case 'Y': return kY; case 'Z': return kZ;
    case '.': return kDot; case ':': return kColon; case '-': return kDash;
    case '/': return kSlash; case '%': return kPct; case '!': return kBang;
    default: return kBlank;  // includes ' ' and anything unsupported
  }
}

}  // namespace mserve_display
